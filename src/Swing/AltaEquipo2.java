public void controlOcultos(){
    if(!cate.getNombrePadre().equals("Computadora") && !cate.getNombrePadre().equals("computadora")){
        this.jBExaminar.setEnabled(false);
        this.jTIP.setEnabled(false);
        this.jLIP.setEnabled(false);
        this.jLArchivo.setEnabled(false);
    }
    if(!cate.getNombrePadre().equals("Computadora") && !cate.getNombrePadre().equals("computadora") && !cate.getNombrePadre().equals("Impresora") && !cate.getNombrePadre().equals("Scanner")){
        this.jCBUsuario.setEnabled(false);
        this.jBnuevoUsu.setEnabled(false);
        this.jLUsuario.setEnabled(false);
    }
}
public void CargarComboUsuarios(){
    ArrayList usuarios; 
    usuarios=IC.listarUsuarios();
    Iterator it = usuarios.iterator();
    while(it.hasNext()){
        Usuario u = (Usuario) it.next();
        this.jCBUsuario.addItem(u.getId()+" "+u.getNombre()+" "+u.getApellido());
    }
}
public void CargarComboLugar(){
    ArrayList lugares; 
    lugares=IC.listarLugares();
    Iterator it = lugares.iterator();
    while(it.hasNext()){
        Lugar l = (Lugar) it.next();
        this.jCBUbicacion.addItem(l.getId()+" "+l.getLocal()+" "+l.getSeccion());
    }


}
public void escribir(String nombreArchivo){

    File f;
    f = new File("nombreArchivo");
    try{
        FileWriter w = new FileWriter(f);
        BufferedWriter bw = new BufferedWriter(w);
        PrintWriter wr = new PrintWriter(bw);  
        wr.write("Esta es una linea de codigo");//escribimos en el archivo
        wr.append(" - y aqui continua"); //concatenamos en el archivo sin borrar lo existente
                //ahora cerramos los flujos de canales de datos, al cerrarlos el archivo quedar치 guardado con informaci칩n escrita
                //de no hacerlo no se escribir치 nada en el archivo
        wr.close();
        bw.close();
    }catch(IOException e){};
}

public static void copy(String origen, String destino) throws IOException {
        Path FROM = Paths.get(origen);
        Path TO = Paths.get(destino);
        //sobreescribir el fichero de destino, si existe, y copiar
        // los atributos, incluyendo los permisos rwx
        CopyOption[] options = new CopyOption[]{
          StandardCopyOption.REPLACE_EXISTING,
          StandardCopyOption.COPY_ATTRIBUTES
        }; 
        Files.copy(FROM, TO, options);
    }
